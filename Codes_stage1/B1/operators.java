public class operators {

	public static void main(String[] args) {
	  int k;// k=;=10;
	  k = 10; k = 100;//10=k;
	  // + - * / % Remainder
	  System.out.print(""+ k%10007 );// 100%10007=100
	  // += -= 8= /= %=
	  k +=10 ; System.out.print(" k ="+ k ); // k=k+10;  / BDMAS
	  k  += 50/2;  System.out.print(" k ="+ k );
	  k = -(-k); System.out.println(" k = "+k); // Negate -
	  boolean b = true;
	  System.out.println("  = "+ !!b);  //Not  -(-10)
	  
	}

}
